class Calculator {
    constructor() {
    }

    addition(num1, num2) {
        return num1  + num2;
    }

    subtraction(num1, num2) {
        return num1  - num2;
    }

    multiply(num1, num2) {
        return num1  * num2;
    }

    division(num1, num2) {
        return num1  / num2;
    }
}


